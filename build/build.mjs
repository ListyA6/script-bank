#!/usr/bin/env node
/**
 * build.mjs — scans scripts/ for markdown files, reads the frontmatter + body,
 * and writes data/scripts.js (a plain JS file the dashboard loads with a <script>
 * tag, so dashboard.html works by double-click with no server).
 *
 * Run:  node build/build.mjs
 * No dependencies.
 */
import { readdirSync, readFileSync, writeFileSync, statSync, mkdirSync } from "node:fs";
import { join, dirname, relative, basename } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const SCRIPTS_DIR = join(ROOT, "scripts");
const OUT_FILE = join(ROOT, "data", "scripts.js");

// Walk a directory recursively, return all .md file paths.
function walk(dir) {
  let out = [];
  let entries;
  try {
    entries = readdirSync(dir, { withFileTypes: true });
  } catch {
    return out;
  }
  for (const e of entries) {
    const full = join(dir, e.name);
    if (e.isDirectory()) out = out.concat(walk(full));
    else if (e.isFile() && e.name.endsWith(".md")) out.push(full);
  }
  return out;
}

// Minimal frontmatter parser for our controlled schema (flat key: value + inline arrays).
function parse(content) {
  const fm = {};
  let body = content;
  if (content.startsWith("---")) {
    const end = content.indexOf("\n---", 3);
    if (end !== -1) {
      const block = content.slice(3, end).trim();
      body = content.slice(end + 4).replace(/^\r?\n/, "");
      for (const raw of block.split(/\r?\n/)) {
        const line = raw.trim();
        if (!line || line.startsWith("#")) continue;
        const i = line.indexOf(":");
        if (i === -1) continue;
        const key = line.slice(0, i).trim();
        let val = line.slice(i + 1).trim();
        if (val === "") { fm[key] = ""; continue; }
        // inline array: [a, b, c]
        if (val.startsWith("[") && val.endsWith("]")) {
          fm[key] = val.slice(1, -1).split(",").map(s => s.trim().replace(/^["']|["']$/g, "")).filter(Boolean);
          continue;
        }
        // strip wrapping quotes
        val = val.replace(/^["']|["']$/g, "");
        fm[key] = val;
      }
    }
  }
  return { fm, body: body.trim() };
}

const files = walk(SCRIPTS_DIR);
const scripts = [];

for (const file of files) {
  const rel = relative(ROOT, file).replace(/\\/g, "/");
  const content = readFileSync(file, "utf8");
  const { fm, body } = parse(content);
  const pillarFromFolder = relative(SCRIPTS_DIR, dirname(file)).split(/[\\/]/)[0] || "uncategorized";
  const mtime = statSync(file).mtime.toISOString();
  scripts.push({
    id: basename(file, ".md"),
    path: rel,
    title: fm.title || basename(file, ".md"),
    pillar: fm.pillar || pillarFromFolder,
    status: (fm.status || "idea").toLowerCase(),
    platform: Array.isArray(fm.platform) ? fm.platform : (fm.platform ? [fm.platform] : []),
    hook_type: fm.hook_type || "",
    length: fm.length || "",
    created: fm.created || "",
    shoot_date: fm.shoot_date || "",
    posted_date: fm.posted_date || "",
    link: fm.link || "",
    priority: (fm.priority || "normal").toLowerCase(),
    modified: mtime,
    body,
  });
}

// Sort: newest created first (stable enough for the board).
scripts.sort((a, b) => (b.created || "").localeCompare(a.created || ""));

mkdirSync(dirname(OUT_FILE), { recursive: true });
const out =
  "// AUTO-GENERATED by build/build.mjs — do not edit by hand.\n" +
  "// Source of truth is the markdown files in scripts/. Run: node build/build.mjs\n" +
  "window.SCRIPTS = " + JSON.stringify(scripts, null, 2) + ";\n" +
  "window.SCRIPTS_BUILT_AT = " + JSON.stringify(new Date().toISOString()) + ";\n";
writeFileSync(OUT_FILE, out, "utf8");

console.log(`Built ${scripts.length} scripts -> ${relative(ROOT, OUT_FILE).replace(/\\/g, "/")}`);
for (const s of scripts) console.log(`  [${s.status.padEnd(10)}] ${s.pillar.padEnd(10)} ${s.title.slice(0, 50)}`);
